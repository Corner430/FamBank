// Simple script to generate tab bar placeholder icons
// Run: node scripts/generate-icons.js
const fs = require('fs');
const path = require('path');

const imagesDir = path.join(__dirname, '..', 'miniprogram', 'images');
if (!fs.existsSync(imagesDir)) {
  fs.mkdirSync(imagesDir, { recursive: true });
}

// Create a minimal 1x1 PNG (81 bytes) as placeholder
const minPng = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
  0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,
  0xDE, 0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41, // IDAT chunk  
  0x54, 0x08, 0xD7, 0x63, 0xF8, 0xCF, 0xC0, 0x00,
  0x00, 0x00, 0x02, 0x00, 0x01, 0xE2, 0x21, 0xBC,
  0x33, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, // IEND chunk
  0x44, 0xAE, 0x42, 0x60, 0x82,
]);

const icons = [
  'tab-home.png', 'tab-home-active.png',
  'tab-bill.png', 'tab-bill-active.png',
  'tab-me.png', 'tab-me-active.png',
];

icons.forEach(name => {
  fs.writeFileSync(path.join(imagesDir, name), minPng);
  console.log(`Created ${name}`);
});
console.log('Done! Replace these with real icons later.');
